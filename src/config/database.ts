import { Pool, PoolConfig } from 'pg';
import dotenv from 'dotenv';

dotenv.config();

const poolConfig: PoolConfig = {
  connectionString: process.env.DATABASE_URL,
  ssl: process.env.NODE_ENV === 'production' ? {
    rejectUnauthorized: false // Railway requires this
  } : false,
  max: 20, // Maximum number of clients in the pool
  idleTimeoutMillis: 30000,
  connectionTimeoutMillis: 2000,
};

export const pool = new Pool(poolConfig);

// Test connection
pool.on('connect', () => {
  console.log('‚úÖ Connected to PostgreSQL database');
});

pool.on('error', (err) => {
  console.error('‚ùå Unexpected error on idle PostgreSQL client', err);
  process.exit(-1);
});

// Graceful shutdown
process.on('SIGINT', async () => {
  await pool.end();
  console.log('üîå PostgreSQL pool has ended');
  process.exit(0);
});

export async function testConnection(): Promise<boolean> {
  try {
    const client = await pool.connect();
    const result = await client.query('SELECT NOW()');
    client.release();
    console.log('‚úÖ Database connection test successful:', result.rows[0]);
    return true;
  } catch (error) {
    console.error('‚ùå Database connection test failed:', error);
    return false;
  }
}

// Helper function to run migrations
export async function runMigration(sql: string): Promise<void> {
  const client = await pool.connect();
  try {
    await client.query('BEGIN');
    await client.query(sql);
    await client.query('COMMIT');
    console.log('‚úÖ Migration executed successfully');
  } catch (error) {
    await client.query('ROLLBACK');
    console.error('‚ùå Migration failed:', error);
    throw error;
  } finally {
    client.release();
  }
}

export default pool;
